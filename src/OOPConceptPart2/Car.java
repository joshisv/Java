package OOPConceptPart2;

public class Car extends Vehicle{
	
	public void start() {
		System.out.println("Car ----------> Started.");
	}
	
	
	public void stop() {
		System.out.println("Car ----------> Stopped.");
	}
	
	
	public void refuel() {
		System.out.println("Car ----------> Refueling...");
	}

}
